  void signUp() async {
    String sid = signupNameController.text.trim().toString();
    String mail = signupEmailController.text.trim().toString();
    String pwd = signupPasswordController.text.trim().toString();
    String pwdConfirm = signupConfirmPasswordController.text.trim().toString();
    int sidN = -1;
    try {
      sidN = int.parse(sid);
    } catch (e) {
      sidN = -1;
    }
    if (sidN == -1) {
      showInSnackBar('学号错误，应该是纯数字');
      return;
    }
    if (!checkMail(mail)) {
      showInSnackBar('邮箱错误...');
      return;
    }
    if (pwd.length < 6) {
      showInSnackBar('密码不少于6位');
      return;
    }
    if (pwd != pwdConfirm) {
      showInSnackBar('两次输入的密码不同');
      return;
    }
    int r = await widget.onSignUp(sid, mail, pwd);
    if (r == 100) {
      showInSnackBar('请前往邮箱激活之后登陆');
      _pageController.animateToPage(0,
          duration: Duration(milliseconds: 300), curve: Cubic(0, 0, 0, 0));
      loginEmailController.text = signupNameController.text;
      signupNameController.text = '';
      loginPasswordController.text = signupPasswordController.text;
      signupPasswordController.text = '';
      signupConfirmPasswordController.text = '';
      signupEmailController.text = '';
    } else if (r == 107) {
      showInSnackBar('该学号已被使用');
    } else if (r == 108) {
      showInSnackBar('该邮箱已被使用');
    }
  }

  void signIn() async {
    String ua = loginEmailController.text.trim().toString();
    String pwd = loginPasswordController.text.trim().toString();
    int uan = -1;
    try {
      uan = int.parse(ua);
    } catch (e) {
      uan = -1;
    }
    if (pwd.length < 6) {
      showInSnackBar('密码不少于6位!');
      return;
    }
    int r = -1;
    if (uan != -1) {
      r = await widget.onSidSignIn(ua.toString(), pwd);
    } else {
      if (checkMail(ua)) {
        r = await widget.onMailSignIn(ua.toString(), pwd);
      } else {
        showInSnackBar('账号格式错误!');
        return;
      }
    }
    showInSnackBar('登陆中...');
//    print(r);
    widget.onCallBack(r,_scaffoldKey.currentContext);
  }

  bool checkMail(String mail) {
    RegExp regExp = RegExp(r"(\w|\d)+@(\w|\d)+\.(\w|\d)+");
    String match = regExp.stringMatch(mail);
    return match == mail;
  }

